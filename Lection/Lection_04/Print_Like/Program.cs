void PrintImage(int[,] image) // Метод печати массива
{

    for (int i = 0; i < image.GetLength(0); i++) // C помощью matrix.GetLength(0) мы взяли количество строк
    {
        for (int j = 0; j < image.GetLength(1); j++) // C помощью matrix.GetLength(1) мы взяли количество столбцов
        {
            //System.Console.Write($"{image[i, j]} ");
            if (image[i, j] == 0) System.Console.Write($" ");
            else System.Console.Write($"+");
        }
        System.Console.WriteLine();
    }
}

void FillImage(int row, int col) // РЕКУРСИЯ - метод вызывает сам себя
{
    if (pic[row, col] == 0)
    {
        pic[row, col] = 1;
        FillImage(row - 1, col); // Сам себя вызывает метод, и закрашивает области, переходя поочередно по крестику влево
        FillImage(row, col - 1);
        FillImage(row + 1, col);
        FillImage(row, col + 1);
    }
}

